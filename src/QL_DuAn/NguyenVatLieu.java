    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        DBAccess acc = new DBAccess();
        if(txtTenNVL.getText().equals("") || txtID.getText().equals("") || txtDongia.getText().equals("") || txtNhacungcap.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin !!");
        }else{
            if(spinSoluong.getValue().hashCode() <= 0 ){
                JOptionPane.showMessageDialog(this, "Số lượng sản phẩm phải lớn hơn 0 !!!");
                spinSoluong.setValue(1);
            }else{
                String IDNVL = txtID.getText();
                String TenNVL = txtTenNVL.getText();
                String Donvitinh = (String) cbbDonvitinh.getSelectedItem();
                String Noicungcap = (String) txtNhacungcap.getText();
                int SoLuong = (int) spinSoluong.getValue();
                int Dongia = Integer.parseInt(txtDongia.getText());
                int TongTien;
                TongTien = Dongia * SoLuong;
                System.out.println(TongTien);
                rs = acc.Query("select IDNVL from QLNguyenVatLieu where IDNVL = '"+IDNVL+"'");
                try {if(rs.next())
                    {
                        JOptionPane.showMessageDialog(this, "Mã vật liệu đã tồn tại !!!");
                    }else{
                        acc.Update("insert into QLNguyenVatLieu values('"+IDNVL+"',N'"+TenNVL+"',N'"+Noicungcap+"','"+SoLuong+"',N'"+Donvitinh+"','"+Dongia+"','"+TongTien+"','"+MaDA+"')");     
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(NguyenVatLieu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } 
        LoadData();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        DBAccess acc = new DBAccess();
        int remove = tabNguyenlieu.getSelectedRow();
        if(remove == -1){
            JOptionPane.showMessageDialog(null, "vui lòng chọn 1 dòng để sửa !!!");
        }else{
            if(txtTenNVL.getText().equals("") || txtDongia.getText().equals("") || txtNhacungcap.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin !!");
            }else{
                if(spinSoluong.getValue().hashCode() <= 0 ){
                    JOptionPane.showMessageDialog(this, "Số lượng sản phẩm phải lớn hơn 0 !!!");
                    spinSoluong.setValue(1);
                }else{
                    String IDNVL = txtID.getText();
                    String TenNVL = txtTenNVL.getText();
                    String Donvitinh = (String) cbbDonvitinh.getSelectedItem();
                    String Noicungcap = (String) txtNhacungcap.getText();
                    int SoLuong = (int) spinSoluong.getValue();
                    int Dongia = Integer.parseInt(txtDongia.getText());
                    int TongTien;
                    TongTien = Dongia * SoLuong;
                    acc.Update("update QLNguyenVatLieu set TenNVL = N'"+TenNVL+"',NoiCungCap = N'"+Noicungcap+"',SoLuongSP = '"+SoLuong+"',"
                            + "DonViTinh = '"+Donvitinh+"',DonGia = '"+Dongia+"',TongTien = '"+TongTien+"' where IDNVL = '"+IDNVL+"' and IDDA = '"+MaDA+"' )");
                }
            } 
            LoadData();
        }   
    }//GEN-LAST:event_btnSuaActionPerformed

