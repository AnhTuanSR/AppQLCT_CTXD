    public void LoadData () 
    {
        DBAccess acc = new DBAccess();
        try { 
            rs = acc.Query("select * from QLNguyenVatLieu where IDDA = '"+MaDA+"'");
            String [] arr = {"Mã nguyên liệu","Tên nguyên liệu","Nơi cung cấp","Số lượng","Đơn vị tính","Đơn giá","Tổng tiền"};
            DefaultTableModel model = new DefaultTableModel(arr,0);
            while(rs.next())
            {
                Vector vec = new Vector();
                vec.add(rs.getString("IDNVL"));
                vec.add(rs.getString("TenNVL"));
                vec.add(rs.getString("NoiCungCap"));
                vec.add(rs.getInt("SoLuongSP"));
                vec.add(rs.getString("DonViTinh"));
                vec.add(rs.getInt("DonGia"));
                vec.add(rs.getInt("TongTien"));
                
                model.addRow(vec);
                
                TTxuatNVL.IDNVL=rs.getString("IDNVL");
                TTxuatNVL.TenNVL=rs.getString("TenNVL");
                TTxuatNVL.NoiCungCap=rs.getString("NoiCungCap");
                TTxuatNVL.SoLuong=rs.getInt("SoLuongSP");
                TTxuatNVL.Donvitinh=rs.getString("DonViTinh");
                TTxuatNVL.Dongia=rs.getInt("DonGia");
                TTxuatNVL.Tongtien=rs.getInt("TongTien");
            }
            tabNguyenlieu.setModel(model);
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(NguyenVatLieu.class.getName()).log(Level.SEVERE, null, ex);
        }
        btnSua.setEnabled(false);
    }
