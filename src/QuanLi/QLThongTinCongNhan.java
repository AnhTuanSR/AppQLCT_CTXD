    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        DBAccess acc = new DBAccess();
        if(txtSDT.getText().equals("") || txtHoten.getText().equals("") || txtDiachi.getText().equals("")){
            JOptionPane.showMessageDialog(this, "vui lòng điền đầy đủ thông tin");
        }else{
            ButtonGroup group = new ButtonGroup();
            group.add(rdbNu);
            group.add(rdbNam);
            String GioiTinh;
            if(rdbNu.isSelected() == true){
                GioiTinh = rdbNu.getText();
            }else{
                GioiTinh = rdbNam.getText();
            }
            String IDCN = txtID.getText();
            String HOTEN = txtHoten.getText();
            String SDT = txtSDT.getText();
            String IDCV =  (String) cbbChucvu.getSelectedItem();
            String Diachi  = txtDiachi.getText();
            try {
                acc.UpdateCN(IDCN, HOTEN, GioiTinh, SDT, IDCV, Diachi);
            } catch (SQLException ex) {
                Logger.getLogger(QLThongTinNhanvien.class.getName()).log(Level.SEVERE, null, ex);
            }
            LoadData();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        DBAccess acc = new DBAccess();
        if(txtID.getText().equals("") || txtSDT.getText().equals("") || txtHoten.getText().equals("") || txtDiachi.getText().equals("")){
            JOptionPane.showMessageDialog(this, "vui lòng điền đầy đủ thông tin");
        }else{
            ButtonGroup group = new ButtonGroup();
            group.add(rdbNu);
            group.add(rdbNam);
            String IDCN = txtID.getText();
            String Hoten = txtHoten.getText();
            String GioiTinh;
            if(rdbNu.isSelected() == true){
                GioiTinh = rdbNu.getText();
            }else{
                GioiTinh = rdbNam.getText();
            }
            String SDT = txtSDT.getText();
            String IDCV =  (String) cbbChucvu.getSelectedItem();
            String Diachi = txtDiachi.getText();
            rs = acc.Query("select IDNV from QLNhanVien where IDNV = '"+IDCN+"'");
            try {
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại !!!");
                }else{
                    acc.InsertCN(IDCN, Hoten, GioiTinh, SDT, IDCV, Diachi);
                }
            } catch (SQLException ex) {
                Logger.getLogger(QLThongTinCongNhan.class.getName()).log(Level.SEVERE, null, ex);
            }
            LoadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tabCongnhanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabCongnhanMouseClicked
        // TODO add your handling code here:
        ButtonGroup group = new ButtonGroup();
        group.add(rdbNu);
        group.add(rdbNam);
        int i = tabCongnhan.getSelectedRow();
        TableModel model = tabCongnhan.getModel();
        cbbChucvu.setSelectedItem(model.getValueAt(i, 4).toString());
        txtID.setText(model.getValueAt(i, 0).toString());
        txtHoten.setText(model.getValueAt(i, 1).toString());
        txtSDT.setText(model.getValueAt(i, 3).toString());
        txtDiachi.setText(model.getValueAt(i, 5).toString());
        if(model.getValueAt(i, 2).toString().equals("Nữ")){
            rdbNu.setSelected(true);
            rdbNam.setSelected(false);
        }else{
            rdbNam.setSelected(true);
            rdbNu.setSelected(false);
        }
        txtID.setEditable(false);
    }//GEN-LAST:event_tabCongnhanMouseClicked

