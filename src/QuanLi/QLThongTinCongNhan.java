    public void LoadData () 
    {
        DBAccess acc = new DBAccess();
        try { 
            rs = acc.Query("select *  from QLCongNhan,ChucVuCongNhan where QLCongNhan.IDCVCN = ChucVuCongNhan.IDCVCN");
            String [] arr = {"Mã công nhân","Họ tên","Giới tính","Số điện thoại","Chức vụ","Địa chỉ"};
            DefaultTableModel model = new DefaultTableModel(arr,0);
            while(rs.next())
            {
                Vector vec = new Vector();
                vec.add(rs.getString("IDCN"));
                vec.add(rs.getString("HoTen"));
                vec.add(rs.getString("GioiTinh"));
                vec.add(rs.getString("SDT"));
                vec.add(rs.getString("ChucVu"));
                vec.add(rs.getString("DiaChi"));
                                  
                model.addRow(vec);
            }
            tabCongnhan.setModel(model);
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(QLThongTinCongNhan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
